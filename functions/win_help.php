    <h2><i class="fas fa-cogs"></i> Settings</h2>
    <h3><i class="fas fa-vector-square"></i> General</h3>
    <h4><i class="fas fa-atlas"></i> Main URL</h4>
    <p>Add your domain here (without the https:// or http://).</p>
    <p>This is used in different areas of the page.</p>
    <h4><i class="far fa-file-alt"></i> Page Title</h4>
    <p>Displays in the tab of the page.</p>
    <h4><i class="fas fa-font"></i> Font Selection</h4>
    <p>Select 1 of 5 different Google fonts for the page:</p>
    <ul>
      <li>Comic Neue</li>
      <li>Open Sans</li>
      <li>Quicksand</li>
      <li>Roboto</li>
      <li>Source Sans Pro</li>
    </ul>
    <h3><i class="far fa-file-image"></i> Weather</h3>
    <p>Configure a weather widget (110px width) at <a href="https://weatherwidget.io" target="_blank">Weatherwidget.io</a></p>
    <p>Paste the code here to update the Weather Widget window.</p>
    <h3><i class="fas fa-envelope-open-text"></i> Messages</h3>
    <p>The message that appears in the Quick Access Bar can be customized here.</p>
    <p>The message that is displayed is determined by the time of day.</p>
    <h3><i class="fas fa-photo-video"></i> Media</h3>
    <p>Wallpaper, Mobile Wallpaper, and Logo can be customized in the Media section of the Settings Menu.</p>
    <h3><i class="fas fa-window-restore"></i> Custom Frames</h3>
    <p>In the Custom Frames section of the Settings Menu, you can customize 5 different iframes.</p>
    <p><b>Note: Some websites have restrictions that prevent their pages from displaying in an iframe.</b></p>
    <p>Customize Options :</p>
    <ul>
      <li>Title</li>
      <li>URL</li>
      <li>Font Awesome Icon</li>
      <li>Window Dimensions</li>
    </ul>
    <h3><i class="fas fa-search-plus"></i> Custom Search</h3>
    <p>In the Custom Search section of the Settings Menu, you can configure up to 24 different searches that will appear in the Multi-Search Window.</p>
    <p><b>Note: To disable a search, clear the fields and type empty into the title.</b></p>
    <p>Customize Options :</p>
    <ul>
      <li>Title</li>
      <li>URL</li>
      <li>Font Awesome Icon</li>
    </ul>
    <h3><i class="fas fa-th-list"></i> Categories</h3>
    <p>In the Categories section of the Settings Menu, unused categories can be enabled/disabled.</p>
    <h3><i class="fas fa-tools"></i> Features</h3>
    <p>In the Features section of the Settings Menu, unused features can be enabled/disabled.</p>
    <h3><i class="fab fa-google"></i> Google Search</h3>
