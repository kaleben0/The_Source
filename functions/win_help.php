    <p>Configure a weather widget (110px width) at <a href="https://weatherwidget.io" target="_blank">Weatherwidget.io</a></p>
    <p>Paste the code here to update the Weather Widget window.</p>

    <h3 id="help_messages"><i class="fas fa-envelope-open-text"></i> Messages</h3>
    <p>The message that appears in the Top Bar can be customized here.</p>
    <p>The message that is displayed is determined by the time of day.</p>

    <h3 id="help_media"><i class="fas fa-photo-video"></i> Media</h3>
    <p>Wallpaper, Mobile Wallpaper, and Avatar/Logo can be customized in the Media section of the Settings Menu.</p>

    <h3 id="help_css"><i class="fab fa-css3-alt"></i> Custom CSS</h3>
    <p>In the Custom CSS section of the Settings Menu, CSS for The Source can be overwritten with your own CSS.</p>

    <h3 id="help_categories"><i class="fas fa-th-list"></i> Custom Categories</h3>
    <p>In the Custom Categories section of the Settings Menu, you can customize 10 different categories.</p>
    <p>Customize Options :</p>
    <ul>
      <li>Name</li>
      <li>Font Awesome Icon</li>
    </ul>

    <h3 id="help_frames"><i class="fas fa-window-restore"></i> Custom Frames</h3>
    <p>In the Custom Frames section of the Settings Menu, you can customize 5 different iframes.</p>
    <p><b>Note: Some websites have restrictions that prevent their pages from displaying in an iframe.</b></p>
    <p>Customize Options :</p>
    <ul>
      <li>Title</li>
      <li>URL</li>
      <li>Font Awesome Icon</li>
      <li>Window Dimensions</li>
    </ul>

    <h3 id="help_search"><i class="fas fa-search-plus"></i> Custom Search</h3>
    <p>In the Custom Search section of the Settings Menu, you can configure up to 24 different searches that will appear in the Multi-Search Window.</p>
    <p>Customize Options :</p>
    <ul>
      <li>Title</li>
      <li>URL</li>
      <li>Font Awesome Icon</li>
    </ul>
    <p><b>Note: To disable a search, clear the fields and type empty into the title.</b></p>

    <h3 id="help_tools"><i class="fas fa-tools"></i> Tools</h3>
    <p>In the Features section of the Settings Menu, unused features can be enabled/disabled.</p>

