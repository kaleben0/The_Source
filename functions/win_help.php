    <h2 id="help_overview">Overview</h2>
    <p>The Source is a customizable start page that provides quick access to tools, search engines and important websites.</p>

    <h3><i class="far fa-bookmark"></i> Bookmarks</h3>
    <p>Bookmarks can be managed by accessing the main directory or a category.</p>
    <p>The main directory can be accessed by clicking a letter on the Directory Bar.</p>

    <h3><i class="fas fa-th-list"></i> Categories</h3>
    <p>Bookmarks here are sorted by one of 10 different custom categories. </p>
    <p>Categories are configured in the settings menu</p>

    <h3><i class="fas fa-window-restore"></i> Frames</h3>
    <p>The Source will display up to 5 customizable iframes.</p>
    <p>Content and dimensions of the iframes can be set up in the Settings Menu.</p>

    <h3><i class="fas fa-envelope-open-text"></i> Messages</h3>
    <p>Custom Messages are displayed on the Top Bar. </p>
    <p>These messages can be configured in the Settings Menu.</p>

    <h3><i class="fas fa-tools"></i> Tools</h3>

    <h4><i class="fab fa-google"></i> Google Search</h4>
    <p>The Google Search bar is located on the Top Bar. </p>
    <p>Queries are sent to Google's search engine.</p>
    <p>The spyglass icon on the left of the bar will clear the input box.</p>
    <p>The spyglass on the right of the bar will launch the Multi-Search window.</p>

    <h4><i class="fas fa-search-plus"></i> Multi-Search</h4>
    <p>The Multi-Search window allows the user to search multiple sites from one interface.</p>
    <p>Queries are sent to search engine button that is click.</p>
    <p>The topmost part of the window contains pre-defined searches for various Google services.</p>
    <p>The bottom most part contains custom searches that can be configured in the settings menu. </p>

    <h4><i class="fas fa-unlock-alt"></i> Password Generator</h4>
    <p>This window will generate a random password.</p>
    <p>The user specifies the length.</p>
    <p>Characters available: </p>
    <p>abcdefghijklmnopqrstuvwxyz0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ!#$%&*+-=?@_ </p>

    <h3><i class="far fa-file-image"></i> Wallpapers</h3>
    <p>Wallpapers can be customized in the Settings Menu.</p>

    <h3><i class="fas fa-cloud-sun"></i> Weather</h3>
    <p>The Weather Window will display a customizable weather forecast.</p>
    <p>The widget can be customized at Weatherwidget.io and configured in the settings menu.</p>


